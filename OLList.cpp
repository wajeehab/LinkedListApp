using namespace std;
#include "OLList.h"

OLList::OLList()
: headM(0)
{
}

OLList::OLList(const OLList& source)
{
    copy(source);
}

OLList& OLList::operator =(const OLList& rhs)
{
    if (this != &rhs) {
        destroy();
        copy(rhs);
    }
    return *this;
}

OLList::~OLList()
{
    destroy();
}

void OLList::print() const
{
    cout << '[';
    if (headM != 0) {
        cout << ' ' << headM->item;
        for (const Node *p = headM->next; p != 0; p = p->next)
            cout << ", " << p->item;
    }
    cout << " ]\n";
}

void OLList::insert(const ListItem& itemA)
{
    Node *new_node = new Node;
    new_node->item = itemA;
    
    if (headM == 0 || itemA <= headM->item ) {
        new_node->next = headM;
        headM = new_node;
        // point one
    }
    else {
        Node *before = headM;      // will point to node in front of new node
        Node *after = headM->next; // will be 0 or point to node after new node
        while(after != 0 && itemA > after->item) {
            before = after;
            after = after->next;
        }
        new_node->next = after;
        before->next = new_node;
        // point two
    }
}

void OLList::remove(const ListItem& itemA)
{
    if (headM == 0 || itemA < headM->item)
        return;
    
    Node *doomed_node = 0;
    
    if (itemA == headM->item) {
        doomed_node = headM;
        headM = headM->next;
    }
    else {
        Node *before = headM;
        Node *maybe_doomed = headM->next;
        while(maybe_doomed != 0 && itemA > maybe_doomed->item) {
            before = maybe_doomed;
            maybe_doomed = maybe_doomed->next;
        }
        Node *nextone=maybe_doomed->next;
		before->next=nextone;
    }
    
}

void OLList::destroy()
{
   Node *remove = headM;
	while (headM!= NULL)
		{
			headM = headM->next;
			delete remove;
			remove = headM;
		}	
	delete remove;

}

void OLList::copy(const OLList& source)
 {
	if (source.headM == 0) 
		headM = 0;
	else{
		headM = new Node;
		Node * s = source.headM;
		Node * copy = headM;
		while (s != 0){
			copy -> item = s -> item;
			if (s -> next !=0){
				copy -> next = new Node;
				copy = copy -> next;
				s = s -> next;
			}
			else {
				copy -> next= 0;
				s = 0;
			}
		}
	}
}



